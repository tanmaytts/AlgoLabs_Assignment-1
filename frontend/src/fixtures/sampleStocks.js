// Derived market summary from the list above
export const sampleMarketSummary = (() => {
  const totalStocks = sampleStocks.length;
  const positiveCount = sampleStocks.filter((s) => s.day_change_pct > 0).length;
  const negativeCount = sampleStocks.filter((s) => s.day_change_pct < 0).length;
  const avgPE = parseFloat(
    (sampleStocks.reduce((sum, s) => sum + s.pe_ratio, 0) / totalStocks).toFixed(2)
  );

  const sorted = [...sampleStocks].sort((a, b) => b.day_change_pct - a.day_change_pct);
  const topGainers = sorted.slice(0, 3).map((s) => ({
    ticker: s.ticker,
    day_change_pct: s.day_change_pct,
    pe_ratio: s.pe_ratio,
  }));
  const topLosers = sorted
    .slice(-3)
    .reverse()
    .map((s) => ({
      ticker: s.ticker,
      day_change_pct: s.day_change_pct,
      pe_ratio: s.pe_ratio,
    }));

  return { totalStocks, positiveCount, negativeCount, avgPE, topGainers, topLosers };
})();

// Sector aggregation derived from the list
export const sampleSectors = (() => {
  const map = {};
  for (const s of sampleStocks) {
    if (!map[s.sector]) {
      map[s.sector] = { sector: s.sector, count: 0, totalMarketCap: 0, tickers: [] };
    }
    map[s.sector].count += 1;
    map[s.sector].totalMarketCap += s.market_cap;
    map[s.sector].tickers.push(s.ticker);
  }
  return Object.values(map);
})();

/**
 * Returns a deterministic random-walk price history for the given ticker and range.
 * Output rows: { date, open, high, low, close, volume }
 *
 * The seed is derived from the ticker string so the same ticker always
 * produces the same sequence across re-renders.
 */
export function generateHistory(ticker, range = '1M') {
  const rowCount = range === '1M' ? 22 : range === '6M' ? 130 : 250;

  // Find base price for the ticker, fall back to 1000
  const stock = sampleStocks.find((s) => s.ticker === ticker);
  const basePrice = stock ? stock.price : 1000;

  // Simple seeded PRNG (mulberry32)
  let seed = 0;
  for (let i = 0; i < ticker.length; i++) {
    seed = (seed * 31 + ticker.charCodeAt(i)) >>> 0;
  }
  function rand() {
    seed = (seed + 0x6d2b79f5) >>> 0;
    let t = Math.imul(seed ^ (seed >>> 15), 1 | seed);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) >>> 0;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  }

  // Build dates going back from today
  const today = new Date();
  const dates = [];
  let d = new Date(today);
  while (dates.length < rowCount) {
    const dow = d.getDay();
    if (dow !== 0 && dow !== 6) {
      dates.unshift(d.toISOString().slice(0, 10));
    }
    d.setDate(d.getDate() - 1);
  }

  // Walk backward from basePrice to get a realistic end value
  // then replay forward
  let price = basePrice;
  const rows = [];
  for (const date of dates) {
    const pctMove = (rand() - 0.5) * 0.03; // +/- 1.5% daily move
    const open = parseFloat((price * (1 + (rand() - 0.5) * 0.005)).toFixed(2));
    price = parseFloat((price * (1 + pctMove)).toFixed(2));
    const close = price;
    const high = parseFloat((Math.max(open, close) * (1 + rand() * 0.012)).toFixed(2));
    const low = parseFloat((Math.min(open, close) * (1 - rand() * 0.012)).toFixed(2));
    const volume = Math.round(500000 + rand() * 9500000);
    rows.push({ date, open, high, low, close, volume });
  }

  return rows;
}
